    private void IgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IgualActionPerformed
        // TODO add your handling code here:
        varDos=(Double.parseDouble(String.valueOf(display.getText())));
        if (contadorSuma>0){
        totalDouble=varUno+varDos;
        display.setText(String.valueOf(totalDouble));
        varUno=0;
        varDos=0;
        contadorSuma=0;
        }
        if (contadorResta>0){
        totalDouble=varUno-varDos;
        display.setText(String.valueOf(totalDouble));
        varUno=0;
        varDos=0;
        contadorResta=0;
        }
        if (contadorMultiplicacion>0){
        totalDouble=varUno*varDos;
        display.setText(String.valueOf(totalDouble));
        varUno=0;
        varDos=0;
        contadorMultiplicacion=0;
        }
        if (contadorDivicion>0){
        totalDouble=varUno/varDos;
        display.setText(String.valueOf(totalDouble));
        varUno=0;
        varDos=0;
        contadorDivicion=0;
        }
        if (contadorPotencia>0){
            double n;
            n=varDos;
        totalDouble=varUno;
        while(n>1){
        totalDouble=totalDouble*varUno;
        n=n-1;
        }
        display.setText(String.valueOf(totalDouble));
        varUno=0;
        varDos=0;
        contadorPotencia=0;
        }
        
        
    }//GEN-LAST:event_IgualActionPerformed

